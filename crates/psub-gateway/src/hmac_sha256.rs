//! HMAC-SHA256 (RFC 2104) with a hand-rolled SHA-256 (FIPS 180-4).
//!
//! Self-contained — does NOT require the `sha2` crate. Useful for
//! environments that want to keep their dependency footprint minimal
//! and where SHA-256 throughput is not on a critical path.
//!
//! The hand-rolled SHA-256 is correct per FIPS 180-4 §6.2 and includes
//! the standard 64-byte block schedule, 8 working variables, and 64
//! round constants. Performance is acceptable for control-plane use
//! (handshake signing, JWT verification) but slower than `sha2`'s
//! table-driven version.

/// 32-byte HMAC-SHA256 tag.
pub type HmacSha256 = [u8; 32];

/// HMAC-SHA256 (single-shot).
pub fn hmac_sha256(key: &[u8], message: &[u8]) -> HmacSha256 {
    const BLOCK_SIZE: usize = 64;
    let key = normalize_key(key);

    let mut ipad = [0x36u8; BLOCK_SIZE];
    let mut opad = [0x5cu8; BLOCK_SIZE];
    for i in 0..BLOCK_SIZE {
        ipad[i] ^= key[i];
        opad[i] ^= key[i];
    }

    let mut inner = Sha256::new();
    inner.update(&ipad);
    inner.update(message);
    let inner_hash = inner.finalize();

    let mut outer = Sha256::new();
    outer.update(&opad);
    outer.update(&inner_hash);
    outer.finalize()
}

fn normalize_key(key: &[u8]) -> [u8; 64] {
    const BLOCK_SIZE: usize = 64;
    let mut k = [0u8; BLOCK_SIZE];
    if key.len() > BLOCK_SIZE {
        let h = sha256(key);
        for i in 0..32 {
            k[i] = h[i];
        }
    } else {
        for (i, &b) in key.iter().enumerate() {
            k[i] = b;
        }
    }
    k
}

